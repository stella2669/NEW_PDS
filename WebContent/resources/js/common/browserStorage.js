
// 로컬 스토리지에서 키에 해당하는 값을 리턴한다.
function getLocalItem(k) {
	return localStorage.getItem(k);
}
// 로컬 스토리지에 키와 값을 저장한다.
function setLocalItem(k, v) {
	localStorage.setItem(k, v);
}
//로컬 스토리지에 키에 해당하는 값을 삭제한다.
function localRemove(k) {
	localStorage.removeItem(k);
}
//로컬 스토리지를 초기화 한다.
function localClear() {
	localStorage.clear();
}


