package egovframework.com.cmm;

import java.io.Serializable;

/**
 * 세션 VO 클래스
 * @author 공통서비스 개발팀 박지욱
 * @since 2009.03.06
 * @version 1.0
 * @see
 *
 * <pre>
 * << 개정이력(Modification Information) >>
 *
 *   수정일      수정자          수정내용
 *  -------    --------    ---------------------------
 *  2009.03.06  박지욱          최초 생성
 *
 *  </pre>
 */
@SuppressWarnings("serial")
public class SessionVO implements Serializable {

	private String userId; // 사용자 ID
	private String password; // 비밀번호
	private String userNm; // 사용자 이름
	private String authCd; // 권한코드
	private String groupCd; // 그룹코드
	private String rankCd;
	private String stCd;
	private String userMail;
	private String resetYn;
	private String loginIp; // 로그인한 사용자 IP

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "SessionVO [userId="+ userId +", password="+ password + ", userNm="+ userNm
				+", authCd="+ authCd+", groupCd="+ groupCd + ", rankCd="+ rankCd +", stCd="+stCd +", userMail="+userMail +"]";
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getUserNm() {
		return userNm;
	}

	public String getResetYn() {
		return resetYn;
	}

	public void setResetYn(String resetYn) {
		this.resetYn = resetYn;
	}

	public void setUserNm(String userNm) {
		this.userNm = userNm;
	}

	public String getAuthCd() {
		return authCd;
	}

	public void setAuthCd(String authCd) {
		this.authCd = authCd;
	}

	public String getGroupCd() {
		return groupCd;
	}

	public void setGroupCd(String groupCd) {
		this.groupCd = groupCd;
	}

	public String getRankCd() {
		return rankCd;
	}

	public void setRankCd(String rankCd) {
		this.rankCd = rankCd;
	}

	public String getStCd() {
		return stCd;
	}

	public void setStCd(String stCd) {
		this.stCd = stCd;
	}

	public String getUserMail() {
		return userMail;
	}

	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}

	public String getLoginIp() {
		return loginIp;
	}

	public void setLoginIp(String loginIp) {
		this.loginIp = loginIp;
	}



}
